void HID_Init()
{
    usbd_dev = usbd_init(&st_usbfs_v1_usb_driver, &dev_descr, &hid_config_descr,
      usb_strings, USB_STRING_COUNT,
      usbd_control_buffer, sizeof(usbd_control_buffer));
}

void HID_Write(s8 *packet, u8 num_channels)
{
	usbd_ep_write_packet(usbd_dev, 0x81, packet, num_channels);
}

extern void (*_HID_Init)();
void HID_Enable() {
#ifdef MODULAR
     _HID_Init = HID_Init;
#endif
    USB_Enable(USB_HID, 1);
}

void HID_Disable() {
#ifdef MODULAR
    _HID_Init = NULL;
#endif
    USB_Disable();
}